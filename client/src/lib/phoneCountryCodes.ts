export function detectCountryFromPhone(phone: string): CountryPhoneInfo | null {
  if (!phone) return null;
  
  const cleaned = phone.replace(/[\s\-\(\)]/g, "");
  
  if (!cleaned.startsWith("+")) {
    if (cleaned.startsWith("00")) {
      const withPlus = "+" + cleaned.substring(2);
      return detectCountryFromPhone(withPlus);
    }
    if (cleaned.startsWith("60") && cleaned.length >= 10) {
      return detectCountryFromPhone("+" + cleaned);
    }
    return null;
  }
  
  for (const country of sortedPrefixes) {
    if (cleaned.startsWith(country.prefix)) {
      return country;
    }
  }
  
  return null;
}

export function getCountryIndicatorText(phone: string): string | null {
  const country = detectCountryFromPhone(phone);
  if (!country) return null;
  return `${country.flag} Possibly from ${country.country}`;
}
